let eventsLoaded = false;
function loadAIEvents() {
  if (eventsLoaded) return;
  eventsLoaded = true;
  const container = document.getElementById('ai-events-container');
  const loading = document.getElementById('ai-events-loading');
  if (!container) return;
  const month = new Date().toLocaleString('it-IT', {month:'long'});
  const langLabel = {it:'italiano',en:'inglese',fr:'francese',de:'tedesco'}[lang] || 'italiano';
  fetch('https://api.anthropic.com/v1/messages', {
    method:'POST',
    headers:{'Content-Type':'application/json'},
    body:JSON.stringify({
      model:'claude-sonnet-4-20250514',
      max_tokens:1000,
      system:'Sei un assistente locale esperto di Villasimius, Sardegna. Rispondi SOLO con un array JSON valido, senza markdown, senza backtick, senza testo aggiuntivo. Ogni oggetto ha: emoji (string), title (string), date (string), description (string, max 90 caratteri).',
      messages:[{role:'user',content:`Elenca 5 eventi, serate o appuntamenti tipici di Villasimius nel mese di ${month}. Includi eventi stagionali, serate nei locali e attività tipiche del periodo. Lingua: ${langLabel}. Rispondi SOLO con array JSON.`}]
    })
  })
  .then(r => r.json())
  .then(d => {
    const text = d.content?.[0]?.text || '[]';
    const events = JSON.parse(text.replace(/```json|```/g,'').trim());
    if (loading) loading.remove();
    events.forEach(ev => {
      const el = document.createElement('div');
      el.className = 'pc';
      el.innerHTML = `<div class="pe">${ev.emoji}</div><div class="pi"><div class="pn">${ev.title}</div><span class="pt tb">${ev.date}</span><div class="pd">${ev.description}</div></div>`;
      container.appendChild(el);
    });
  })
  .catch(() => {
    if (loading) loading.textContent = T[lang].ev_loading.replace('⏳','').trim() + ' — ' + (lang === 'it' ? 'riprova più tardi.' : 'try again later.');
  });
}

// ── CHECKLIST ──
function initChecklist() {
  const items = document.querySelectorAll('.chk-item');
  items.forEach(item => {
    item.addEventListener('click', function() {
      this.classList.toggle('done');
      checkAllDone();
    });
  });
  const reset = document.getElementById('chk-reset');
  if (reset) reset.addEventListener('click', () => {
    items.forEach(i => i.classList.remove('done'));
    const msg = document.getElementById('co-done-msg');
    if (msg) msg.style.display = 'none';
  });
}

function checkAllDone() {
  const items = document.querySelectorAll('.chk-item');
  const allDone = [...items].every(i => i.classList.contains('done'));
  const msg = document.getElementById('co-done-msg');
  if (msg) msg.style.display = allDone ? 'block' : 'none';
}

// ── GALLERY ──
function initGalleries() {
  document.querySelectorAll('.gal').forEach(gal => {
    const photos = JSON.parse(gal.getAttribute('data-photos'));
    if (!photos || !photos.length) return;
    const track = document.createElement('div');
    track.className = 'gal-track';
    photos.forEach(src => {
      const img = document.createElement('img');
      img.className = 'gal-slide'; img.src = src; img.loading = 'lazy';
      track.appendChild(img);
    });
    gal.appendChild(track);
    if (photos.length === 1) { gal.classList.add('gal-single'); return; }
    const prev = document.createElement('button'); prev.className = 'gal-btn prev'; prev.innerHTML = '&#8249;';
    const next = document.createElement('button'); next.className = 'gal-btn next'; next.innerHTML = '&#8250;';
    const dotsEl = document.createElement('div'); dotsEl.className = 'gal-dots';
    photos.forEach((_, i) => { const d = document.createElement('span'); d.className = 'gal-dot'+(i===0?' on':''); dotsEl.appendChild(d); });
    gal.appendChild(prev); gal.appendChild(next); gal.appendChild(dotsEl);
    let ci = 0;
    function goTo(n) {
      ci = (n + photos.length) % photos.length;
      track.style.transform = `translateX(-${ci*100}%)`;
      dotsEl.querySelectorAll('.gal-dot').forEach((d,i) => d.classList.toggle('on', i===ci));
    }
    prev.addEventListener('click', e => { e.stopPropagation(); goTo(ci-1); });
    next.addEventListener('click', e => { e.stopPropagation(); goTo(ci+1); });
    dotsEl.querySelectorAll('.gal-dot').forEach((d,i) => d.addEventListener('click', () => goTo(i)));
    let sx = 0;
    gal.addEventListener('touchstart', e => { sx = e.touches[0].clientX; }, {passive:true});
    gal.addEventListener('touchend', e => { const dx = e.changedTouches[0].clientX - sx; if (Math.abs(dx)>40) goTo(dx<0?ci+1:ci-1); }, {passive:true});
  });
}

