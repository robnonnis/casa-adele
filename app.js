let lang = 'it';
let cur = 'benvenuto';

function setLang(l) {
  lang = l;
  const d = T[l];
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.getAttribute('data-t');
    if (d[key] !== undefined) el.innerHTML = d[key];
  });
  document.querySelectorAll('.lbtn').forEach(b => b.classList.toggle('active', b.getAttribute('data-lang') === l));
  const waDaniela = document.getElementById('wa-daniela');
  const waEgidio = document.getElementById('wa-egidio');
  if (waDaniela) waDaniela.href = 'https://wa.me/393466225194?text=' + d.wa_msg_daniela;
  if (waEgidio) waEgidio.href = 'https://wa.me/393408379789?text=' + d.wa_msg_egidio;
  document.documentElement.lang = l;
  const revBtn = document.getElementById('wifi-reveal');
  if (revBtn) revBtn.textContent = d.info_wifi_show;
  syncNav(cur);
}

function go(id) {
  document.querySelectorAll('.sec').forEach(s => s.classList.remove('active'));
  document.getElementById(id).classList.add('active');
  cur = id;
  syncNav(id);
  if (id === 'eventi' && !eventsLoaded) loadAIEvents();
  window.scrollTo({ top: document.getElementById('mainNav').offsetTop - 1, behavior: 'smooth' });
}

function syncNav(id) {
  document.querySelectorAll('.nbtn').forEach(b => b.classList.toggle('active', b.getAttribute('data-section') === id));
}

